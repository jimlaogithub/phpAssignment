        2. if…else	
        If(condition){
        //codes run here;
        }else{
        //codes run here;
        }
        
        3. if…else if…else	
        If(condition){
            //codes run here;
        }else if(condition){
            //codes run here;
        }else{
            //codes run here;
        }
        
        4. switch…case	
        Switch(expression){
        Case label1:
            //code to be run if expression = label1;
            break;
        Case label2:
            //code to be run if expression = label1;
            break;
        Default:
            //code to be run if expression is diff from all label;
        }

        5. for loop	
        for (init counter; test counter; increment counter) {
            code to be executed for each iteration;
        }

        6. do while loop	
        do {
            code to be executed;
        } while (condition is true);
        
        7. while loop	
        while (condition is true) {
            code to be executed;
        }

        8. foreach loop	
        foreach ($array as $value) {
            code to be executed;
        }

        
        9. break statement	
        break;
        
        10. continue statement	
        continue;
        
        11. try…catch	

        try {
           //if the exception is not true codes will run here;
        }
        //catch exception
        catch(Exception $e) {
        //if exception is catch codes runs here;
        }
